def biodata(name, age, address = None, hobbies = None, 
            is_married = None, list_school = None, skills = None, interest_in_coding = None):
    {'name':name,
    'age': age,
    'address': address,
    'hobbies':list(hobbies),
    'is_married': is_married,
    'list_school': list(list_school),
    'skills':list(skills),
    'interest_in_coding': interest_in_coding}